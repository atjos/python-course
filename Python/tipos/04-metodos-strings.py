comida = "fresas con crema"
print(comida.upper())  # Convierte a mayúsculas
print(comida.lower())  # Convierte a minúsculas
print(comida.capitalize())  # Convierte la primera letra a mayúscula
print(comida.title())  # Convierte la primera letra de cada palabra a mayúscula
print(comida.find("s")) # Busca la posición de la primera "s"
print(comida.replace("crema", "chocolate")) # Reemplaza "crema" por "chocolate"
print("con" in comida)  # Verifica si "con" está en la cadena
